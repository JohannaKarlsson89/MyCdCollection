using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using MyCdCollection;
using MyCdCollection.Data;

namespace MyCdCollection.Controllers
{
    public class AlbumController : Controller
    {
        private readonly CdContext _context;

        public AlbumController(CdContext context)
        {
            _context = context;
        }

//public async Task<IActionResult> Index(string sortOrder)
public async Task<IActionResult> Index(string sortOrder, string searchString)
{
    
    ViewData["NameSortParm"] = String.IsNullOrEmpty(sortOrder) ? "name_desc" : "";
     ViewData["CurrentFilter"] = searchString;
   // ViewData["DateSortParm"] = sortOrder == "Date" ? "date_desc" : "Date";
    var cd = from s in _context.Album.Include(a => a.Artist) select s;

       if (!String.IsNullOrEmpty(searchString))
    {
        cd = cd.Where(s => s.AlbumName.Contains(searchString));
                              // || s.FirstMidName.Contains(searchString));
    }
    switch (sortOrder)
    {
        case "name_desc":
            cd = cd.OrderByDescending(s => s.AlbumName);
            break;
        default:
            cd = cd.OrderBy(s => s.AlbumName);
            break;
    }
    return View(await cd.AsNoTracking().ToListAsync());
   
}

